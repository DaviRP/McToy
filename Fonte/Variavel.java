class Variavel{


    public String nome;
    public double valor;








    public Variavel(String N){

        this.nome=N;
        this.valor=0;
    }

        

    public String getNome(){

        return this.nome;

    }
    public void setNome(String N){

        this.nome=N;

    }
    public double getValor(){

        return this.valor;


    }
    public void setValor(double N){


        this.valor=N;


    }






}
